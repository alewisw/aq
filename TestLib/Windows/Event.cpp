//------------------------------------------------------------------------------
Event::Event(void)
{
    m_hEvent = CreateEvent(NULL, TRUE, FALSE, NULL);
}

//------------------------------------------------------------------------------
Event::~Event(void)
{
    CloseHandle(m_hEvent);
}

//------------------------------------------------------------------------------
void Event::set(void)
{
    SetEvent(m_hEvent);
}

//------------------------------------------------------------------------------
void Event::reset(void)
{
    ResetEvent(m_hEvent);
}

//------------------------------------------------------------------------------
void Event::block(void)
{
    WaitForSingleObject(m_hEvent, INFINITE);
}




