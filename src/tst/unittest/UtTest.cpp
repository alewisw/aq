TEST_SUITE(UtTest);

//------------------------------------------------------------------------------
TEST(give_X50Y20_when_DecomposeXOpY_then_StringValid)
{
    int x = 50;
    int y = 20;

    TestAssert::Decomposer d;

    d >= x > y;
    REQUIRE(d.str() == "50 > 20");

    d >= x < y;
    REQUIRE(d.str() == "50 < 20");

    d >= x >= y;
    REQUIRE(d.str() == "50 >= 20");

    d >= x <= y;
    REQUIRE(d.str() == "50 <= 20");

    d >= x == y;
    REQUIRE(d.str() == "50 == 20");

    d >= x != y;
    REQUIRE(d.str() == "50 != 20");
}

//------------------------------------------------------------------------------
TEST(give_X50Y20_when_DecomposeBracketedXOpY_then_StringEmpty)
{
    int x = 50;
    int y = 20;

    TestAssert::Decomposer d;

    d >= (x > y);
    REQUIRE(d.str() == "");
}

//------------------------------------------------------------------------------
TEST(give_BoolTest_when_Decompose_then_StringEmpty)
{
    bool b = true;

    TestAssert::Decomposer d;

    d >= b;
    REQUIRE(d.str() == "");

    d >= !b;
    REQUIRE(d.str() == "");
}




